#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ptr;
	int		size_src;
	int		i;

	i = 0;
	size_src = ft_strlen(s1);
	ptr = malloc(sizeof(char) * (size_src + 1));
	if (!(ptr))
		return (NULL);
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
